export type EquityStrings = (typeof STRINGS)[Locale];

export function getEquityStrings(locale: Locale): EquityStrings {
  return STRINGS[locale];
}

export function translateBackendReason(locale: Locale, reason: string): string {
  const map = STRINGS[locale].backendReasons as Record<string, string>;
  return map[reason] ?? reason;
}

export function layerSubtitle(locale: Locale, code: "L5" | "L6", bench: string): string {
  const sub = STRINGS[locale].layers[code].sub;
  return sub.replace("{bench}", bench);
}
